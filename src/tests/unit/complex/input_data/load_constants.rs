impl LoadConstantArray {
    pub fn from(src: Vec<(f64, f64, f64, i32)>) -> Self {
        Self {
            data: src
                .into_iter()
                .map(
                    |(mass, bound_x1, bound_x2, loading_type)| LoadConstantData {
                        mass,
                        bound_x1,
                        bound_x2,
                        loading_type: match loading_type {
                            20 => LoadConstantType::Hull,
                            _ => LoadConstantType::Equipment,
                        },
                    },
                )
                .collect(),
            error: HashMap::new(),
        }
    }
}

